package package1;

public class Vehicle {
    boolean isHasWheels;
    boolean isHasScrew;
    boolean isHasWings;
    boolean isCarryGoods;

    public Vehicle (boolean isHasWheels, boolean isHasScrew, boolean isHasWings, boolean isCarryGoods) {
        this.isHasWheels = isHasWheels;
        this.isHasScrew = isHasScrew;
        this.isHasWings = isHasWings;
        this.isCarryGoods = isCarryGoods;
    }
}

